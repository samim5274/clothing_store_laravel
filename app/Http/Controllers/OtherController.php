     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $obj = new Other();
        $obj->name = $request->has('name')? $request->get('name'):'';
        $obj->uid = $request->has('uid')? $request->get('uid'):'';
        $obj->save();
        return back()->with('success','Other data insert successfully.');
    }

